// Принципы цветовых сочетаний — что хорошо дополняет каждый цвет.
// Зелёный считаем универсальным наполнителем (эвкалипт, гипсофила) —
// он подходит ко всем, поэтому в пересечении игнорируется.
const COLOR_COMPANIONS = {
  'белый': ['розовый', 'красный', 'жёлтый', 'оранжевый'],
  'розовый': ['белый', 'красный', 'фиолетовый'],
  'красный': ['белый', 'розовый', 'зелёный'],
  'жёлтый': ['белый', 'оранжевый', 'зелёный'],
  'оранжевый': ['жёлтый', 'белый', 'красный'],
  'фиолетовый': ['белый', 'розовый'],
  'голубой': ['белый', 'розовый'],
  'зелёный': ['белый', 'розовый', 'красный', 'жёлтый', 'оранжевый'],
}

// Получить анализ цветовой гаммы букета
export function getColorPalette(flowerColors) {
  if (!flowerColors || flowerColors.length === 0) {
    return { currentColors: [], suggestions: [], harmony: null }
  }

  const unique = [...new Set(flowerColors.map((c) => c.toLowerCase()))]
  const currentColors = unique
    .map((key) => {
      const info = COLOR_PALETTE[key]
      return info ? { key, ...info } : null
    })
    .filter(Boolean)

  // Строгое пересечение: показываем только те цвета, которые
  // подходят КО ВСЕМ цветам, уже присутствующим в букете.
  // Это даёт надёжную рекомендацию — добавленный цвет точно не сконфликтует.
  let suggestionSet = null
  for (const color of unique) {
    const companions = new Set(COLOR_COMPANIONS[color] || [])
    if (suggestionSet === null) {
      suggestionSet = companions
    } else {
      // оставляем только те, что есть и здесь, и в накопленном множестве
      suggestionSet = new Set([...suggestionSet].filter((c) => companions.has(c)))
    }
  }

  // Убираем цвета, которые уже есть в букете
  const suggestions = [...(suggestionSet || [])]
    .filter((c) => !unique.includes(c))
    .map((key) => ({ key, ...COLOR_PALETTE[key] }))
    .filter((c) => c.label)

  // Подсказка по характеру гаммы
  let harmony = null
  if (suggestions.length === 0 && unique.length >= 2) {
    harmony = 'Состав сбалансирован — выбранные цвета хорошо дополняют друг друга.'
  } else if (unique.length === 1) {
    harmony = 'Монохромная гамма — выглядит цельно и элегантно. Можно добавить контрастный акцент.'
  } else if (unique.length === 2) {
    harmony = 'Двухцветная гамма — классическое решение. Добавьте зелень для баланса.'
  } else if (unique.length >= 4) {
    harmony = 'Многоцветная композиция — яркая и насыщенная. Следите за балансом тонов.'
  } else {
    harmony = 'Сбалансированная гамма из трёх цветов — выглядит гармонично.'
  }

  return { currentColors, suggestions, harmony }
}

// Получить рекомендации для букета (по списку типов цветов в составе)
export function getBouquetTips(flowerTypes) {
  if (!flowerTypes || flowerTypes.length === 0) {
    return {
      summary: null,
      careTips: [],
      suggestions: [],
      warnings: [],
      flowerCareCards: [],
    }
  }

  // Уникальные типы (в нижнем регистре, для поиска в таблице)
  const unique = [...new Set(flowerTypes.map((t) => t.toLowerCase()))]

  // Парные мягкие советы по уходу
  const careTips = []
  for (const pairTip of PAIR_CARE_TIPS) {
    const [a, b] = pairTip.pair
    if (unique.includes(a) && unique.includes(b)) {
      careTips.push(pairTip.tip)
    }
  }

  // Предупреждения о совместимости — реальные конфликты
  const warnings = []
  for (const w of COMPATIBILITY_WARNINGS) {
    const [a, b] = w.pair
    if (unique.includes(a) && unique.includes(b)) {
      warnings.push({
        text: w.warning,
        severity: w.severity,
      })
    }
  }

  // Карточки ухода за каждым цветком в букете (по B-варианту)
  const flowerCareCards = unique
    .map((type) => {
      const info = FLOWER_TIPS[type]
      if (!info) return null
      return {
        key: type,
        label: info.label,
        description: info.description,
        care: info.care,
      }
    })
    .filter(Boolean)

  // Что можно добавить — рекомендации цветов, которые сочетаются со всеми текущими
  const suggestionCounts = {}
  for (const type of unique) {
    const info = FLOWER_TIPS[type]
    if (!info) continue
    for (const recommended of info.goodWith) {
      if (!unique.includes(recommended)) {
        suggestionCounts[recommended] = (suggestionCounts[recommended] || 0) + 1
      }
    }
  }
  // Сортируем — сначала те цветы, которые сочетаются с бóльшим количеством текущих
  const suggestions = Object.entries(suggestionCounts)
    .sort((a, b) => b[1] - a[1])
    .slice(0, 4)
    .map(([flower]) => {
      const info = FLOWER_TIPS[flower]
      return info ? info.label : flower
    })

  // Краткое описание состава
  let summary
  if (unique.length === 1) {
    const info = FLOWER_TIPS[unique[0]]
    summary = info ? `В вашем букете только ${info.label.toLowerCase()}` : null
  } else {
    const labels = unique.map((t) => {
      const info = FLOWER_TIPS[t]
      return info ? info.label.toLowerCase() : t
    })
    summary = `В вашем букете: ${labels.join(', ')}`
  }

  return {
    summary,
    careTips,
    suggestions,
    warnings,
    flowerCareCards,
  }
}
